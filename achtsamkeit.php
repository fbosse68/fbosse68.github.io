            <h2>Eine Einladung</h2>
            <p>Achtsamkeit ist keine Pflichtübung. Sie ist eine Einladung. Eine Einladung, aus dem automatischen Reagieren auszusteigen. Eine Einladung, dem Körper wieder zu begegnen – mit Freundlichkeit. Eine Einladung, dem Moment zu begegnen – diesem Moment, genau so wie er ist.</p>
            
            <p>Nicht um ihn zu reparieren. Nicht um ihn zu verändern. Sondern um ihm zu begegnen. Mit Offenheit. Mit Herz. Mit der Bereitschaft, da zu sein mit dem, was ist.</p>
            
            <p>Das ist nicht einfach. Das ist vielleicht das Schwierigste überhaupt. Aber es ist auch das, was Heilung ermöglicht – nicht im Sinne von "alles wird gut", sondern im Sinne von "ich bin nicht mehr so allein damit". Im Sinne von "ich kann dem begegnen, was ist". Im Sinne von "es gibt wieder Raum".</p>
            
            <p>Und das ist die Art, wie ich arbeite.</p>
