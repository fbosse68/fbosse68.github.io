            <p>Aber MBSR ist mehr als eine Sammlung von Techniken. Es ist ein Weg, diese Haltung – diese Qualität von mitfühlendem Gewahrsein – systematisch zu kultivieren. In Gemeinschaft mit anderen. Mit Anleitung und Struktur. Mit Zeit zum Üben und Zeit zum Reflektieren.</p>
            
            <p>Das ist ein möglicher Weg. Aber auch in der Einzelbegleitung ist Achtsamkeit das tragende Element: nicht als Technik, sondern als Weise des Seins.</p>
        </div>

